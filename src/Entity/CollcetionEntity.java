package Entity;

public class CollcetionEntity {

	private	int	CollectId	;//	收藏id
	private	String	CollectName	;//	收藏名字
	private	String	CollectTime	;//	收藏时间
	private	int	CollectState	;//	收藏状态
	private	int	CollectStyle	;//	收藏类型
	private	int	CollectStyleId	;//	收藏类型id
	private	int	UserId	;//	用户id
	
	public CollcetionEntity() {}

	
	
	
	
	public int getCollectId() {
		return CollectId;
	}

	public void setCollectId(int collectId) {
		CollectId = collectId;
	}

	public String getCollectName() {
		return CollectName;
	}

	public void setCollectName(String collectName) {
		CollectName = collectName;
	}

	public String getCollectTime() {
		return CollectTime;
	}

	public void setCollectTime(String collectTime) {
		CollectTime = collectTime;
	}

	public int getCollectState() {
		return CollectState;
	}

	public void setCollectState(int collectState) {
		CollectState = collectState;
	}

	public int getCollectStyle() {
		return CollectStyle;
	}

	public void setCollectStyle(int collectStyle) {
		CollectStyle = collectStyle;
	}

	public int getCollectStyleId() {
		return CollectStyleId;
	}

	public void setCollectStyleId(int collectStyleId) {
		CollectStyleId = collectStyleId;
	}

	public int getUserId() {
		return UserId;
	}

	public void setUserId(int userId) {
		UserId = userId;
	}
	
	
	
	
}
