package Entity;

import java.util.ArrayList;
import java.util.List;

public class SetCheckInfoListEntity {

private List<CheckInfoEntity> CheckInfoList = new ArrayList<CheckInfoEntity>();
	
	public SetCheckInfoListEntity(List<CheckInfoEntity> CheckInfoList){
		this.CheckInfoList = CheckInfoList;
	}
	
	public SetCheckInfoListEntity() {
	}

	public int getSize(){
		return CheckInfoList.size();
	}
	
	public CheckInfoEntity getItem(int arg0){
		return CheckInfoList.get(arg0);
	}
}
